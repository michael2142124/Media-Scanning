@app.route("/download")
def download():
    file_path = "crime_data_final.xlsx"
    if not os.path.exists(file_path):
        return jsonify({"error": "No file found. Run /scrape first."}), 404
    return send_file(
        file_path,
        as_attachment=True,
        mimetype="application/vnd.openxmlformats-officedocument.spreadsheetml.sheet"
    )

